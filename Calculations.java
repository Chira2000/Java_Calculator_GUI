
package calculatorgui;


public class Calculations {
    

    public int ADD(int a,int b){
        
        int add=a+b;
        
        
        return add;
    
    
    
    
    }
    
    
    public int SUB(int a,int b){
    
    int sub=a-b;
    
    return sub;
    
    }
    
    
    public int MUL(int a,int b){
    
    int mul=a*b;
    
    
    return mul;
    
    }
    
    public float DIV(int a,int b){
        
        float div=(a/b);
        
        return div;
    
    
    
    
    }
    
    
}
